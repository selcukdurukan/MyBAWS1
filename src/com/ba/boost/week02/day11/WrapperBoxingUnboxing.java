package com.ba.boost.week02.day11;

public class WrapperBoxingUnboxing {

	public static void main(String[] args) {
		
		
		Integer a =  5;
		int b = (int)a; // Unboxing --> From wrapper class to primitive type.
		
		int d= 10;
		Integer c= (Integer)d; // Boxing ---> From primitive type to wrapper class.
		
		//Wrapper classes have a lot of methods.
	}

}
